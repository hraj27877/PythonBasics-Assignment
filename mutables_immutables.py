#Mutables:- It can be changed and modified after it is created.

#Example of Mutables: 

# List (mutable)
my_list = [1, 2, 3]
print("Original list:", my_list)

# Modifying the list
my_list.append(4)
print("Modified list:", my_list)

# Changing an element
my_list[0] = 10
print("After changing an element:", my_list)
